def square(n):
    """Return the square of a number."""
    return n * n


def is_even(n):
    """Return True if the number is even, False otherwise."""
    return n % 2 == 0


def celsius_to_fahrenheit(c):
    """Convert Celsius to Fahrenheit using F = (C * 9/5) + 32."""
    return (c * 9 / 5) + 32


def greet(name):
    """Return a personalized greeting string."""
    return f"Hello, {name}!"
